function calcularTudo() {

  let cargaNumerica = parseInt(valorCarregado.textContent) || 0;
  let inputCusto = parseFloat(document.getElementById('custo-do-KWh').value) || 0;
  let inputVenda = parseFloat(document.getElementById('venda-do-KWh').value) || 0;
  let valorFinal = parseInt(document.getElementById('fim').value) || 0;
  let taxaDesbloqueio = parseFloat(document.getElementById('taxa-de-desbloqueio').value || 0)

  // Calcular Energia Gasta
  resultadoEnergiaGasta = (cargaNumerica * bateria_kWh) / 100;
  document.getElementById('energia-gasta').value = resultadoEnergiaGasta.toFixed(2);

  // Calcular Custo Total
  resultadoCustoTotal = inputCusto * resultadoEnergiaGasta;
  document.getElementById('custo-total').value = resultadoCustoTotal.toFixed(2);

  // Calcular Venda Total
  resultadoVendaTotal = (inputVenda * resultadoEnergiaGasta) + taxaDesbloqueio;
  document.getElementById('venda-total').value = resultadoVendaTotal.toFixed(2);

  // Calcular Autonomia Ganha
  resultadoAutonomiaGanha = (cargaNumerica * autonomia_km) / 100;
  document.getElementById('autonomia-ganha').value = resultadoAutonomiaGanha.toFixed(2);

  // Calcular Autonomia Final
  resultadoAutonomiaFinal = (valorFinal * autonomia_km) / 100;
  document.getElementById('autonomia-final').value = resultadoAutonomiaFinal.toFixed(2);

  calculosAC();
  calculosDC();

}

