function atualizarSlider() {
  let inicio = parseInt(inicioInput.value) || 0;
  let fim = parseInt(fimInput.value) || 0;

  if (inicio < 0) inicio = 0;
  if (fim > 100) fim = 100;
  if (fim < inicio) fim = inicio;

  let carga = fim - inicio;
  slider.value = carga;
  valorCarregado.textContent = carga + "%";

  inicioInput.value = inicio;
  fimInput.value = fim;

  calcularTudo();
}

function atualizarInputs() {
  let inicio = parseInt(inicioInput.value) || 0;
  let carga = parseInt(slider.value) || 0;
  let fim = inicio + carga;

  if (fim > 100) fim = 100;

  fimInput.value = fim;
  valorCarregado.textContent = carga + "%";


  calcularTudo();
}


